#   Leia a quantidade de cavalos que um haras possui e faça 
#   um algoritmo para calcular quantas ferraduras são necessárias
#   para equipar todos os cavalos do haras.

quant_cavalos = int(input("Informe a quantidade de cavalos: "))

ferraduras = quant_cavalos * 4

print (f'É necessario {ferraduras} ferradura(s) para equipar o(s) {quant_cavalos} cavalo(s)')
